    private void init() {
        try {
            repository = RepositoryFactory.getRepository();
            users = repository.selectUsers(); // metoda koja vraća sve korisnike iz baze

            String[] columns = {"UserID", "First Name", "Last Name", "Username", "Email", "isAdmin"};
            Object[][] data = new Object[users.size()][6];

            for (int i = 0; i < users.size(); i++) {
                Users u = users.get(i);
                data[i][0] = u.getId();
                data[i][1] = u.getFirstName();
                data[i][2] = u.getLastName();
                data[i][3] = u.getUserName();
                data[i][4] = u.getEmail();
                data[i][5] = u.isAdmin();
            }

            

            JScrollPane scrollPane = new JScrollPane(tblUsers);
            getContentPane().add(scrollPane, java.awt.BorderLayout.CENTER);

            
            setTitle("Edit Roles");
            setSize(950, 650);
            setLocationRelativeTo(null);
        } catch (Exception ex) {
            ex.printStackTrace();
            MessageUtils.showErrorMessage("Error", "Cannot load users");
        }
    }

    

    private void initListeners() {
        tbUsers.getSelectionModel().addListSelectionListener(e -> {
            if (!e.getValueIsAdjusting() && tbUsers.getSelectedRow() >= 0) {
                int row = tbUsers.convertRowIndexToModel(tbUsers.getSelectedRow());
                Users u = users.get(row);
                selectedUserId = u.getId();
                tpFirstName.setText(u.getFirstName());
                tpLastName.setText(u.getLastName());
                cbIsAdmin.setSelected(u.isAdmin());
            }
        });
    }

    private void loadTable() {
        try {
            users = repository.selectUsers();
            String[] cols = {"ID", "First Name", "Last Name", "Username", "E-mail", "Admin"};
            DefaultTableModel tm = new DefaultTableModel(cols, 0) {
                @Override
                public boolean isCellEditable(int r, int c) {
                    return false;
                }

                @Override
                public Class<?> getColumnClass(int c) {
                    return c == 5 ? Boolean.class : String.class;
                }
            };
            for (Users u : users) {
                tm.addRow(new Object[]{
                    u.getId(),
                    u.getFirstName(),
                    u.getLastName(),
                    u.getUserName(),
                    u.getEmail(),
                    u.isAdmin()
                });
            }
            tbUsers.setModel(tm);
            tbUsers.setSelectionMode(ListSelectionModel.SINGLE_SELECTION);
        } catch (Exception ex) {
            ex.printStackTrace();
            MessageUtils.showErrorMessage("Error", "Cannot load users!");
        }
    }

