    private void btnSaveChangeActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveChangeActionPerformed
        if (selectedUserId == -1) {
            MessageUtils.showInformationMessage("Info", "Please select a user first!");
            return;
        }

        boolean showConfirmDialog = MessageUtils.showConfirmDialog("T'fck are you doing", "Are you really really sure");

        if (showConfirmDialog == true) {
            try {
                boolean newAdmin = cbIsAdmin.isSelected();
                repository.updateUserRole(selectedUserId, newAdmin);

                MessageUtils.showInformationMessage("Success", "Role updated!");
                loadTable();                 // osvježi tablicu
                // po želji: clearForm();
            } catch (Exception ex) {
                ex.printStackTrace();
                MessageUtils.showErrorMessage("Error", "Failed to update role");
            }
        }
    }//GEN-LAST:event_btnSaveChangeActionPerformed

