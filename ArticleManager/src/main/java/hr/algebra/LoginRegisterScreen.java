    private void btnLoginActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnLoginActionPerformed

        String username = tfUsername.getText().trim();
        String userPass = tfPassword.getText().trim();

        try {
            Optional<Users> logIn = repository.logIn(username, userPass);
            Optional<Users> loginResult = repository.logIn(username, userPass);

            if (logIn.isPresent()) {
                // Ako je login uspje≈°an, otvori glavnu formu
                Users user = loginResult.get();
                new ArticleManager(user).setVisible(true); // ili tvoja glavna forma
                this.dispose(); // zatvori login formu
            } else {
                JOptionPane.showMessageDialog(this,
                        "Wrong username or password! Please try again!",
                        "Login Failed",
                        JOptionPane.ERROR_MESSAGE);
            }

        } catch (Exception ex) {
            ex.printStackTrace();
            JOptionPane.showMessageDialog(this,
                    "An error occurred while logging in.",
                    "Error",
                    JOptionPane.ERROR_MESSAGE);
        }


    }//GEN-LAST:event_btnLoginActionPerformed

